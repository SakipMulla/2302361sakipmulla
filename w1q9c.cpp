//display a message "well done" n number of times (while/do while/for)

#include <iostream>
using namespace std;

int main()
 {
    int n;
    cout<<"for loop !!"<<endl;
    cout << "Enter the number of times to display the message: =";
    cin >> n;

   for (int i = 0; i < n; i++)
   {
    cout<<"well done!!"<<endl;
   }  
     
    return 0;
}
